var myWindow ;

function openWindow()
{
    myWindow = window.open("www.google.com", "" ,"width=500px,height=200px");

    myWindow.document.write("<p>This is my Window</p>")
} 
function moveWindow()
{
    myWindow.moveBy(100,100);
    myWindow.focus();
}

